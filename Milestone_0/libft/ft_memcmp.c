#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned const char		*ss1;
	unsigned const char		*ss2;
	size_t					i;

	ss1 = (unsigned const char *)s1;
	ss2 = (unsigned const char *)s2;
	i = 0;
	while (i < n)
	{
		if (ss1[i] != ss2[i])
		{
			return (ss1[i] - ss2[i]);
		}
		i++;
	}
	return (0);
}
